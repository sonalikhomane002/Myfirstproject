using System;
using System.Collections.Generic;
using System.Text;

namespace Myfirstproject.ConditionalDemo
{
    class Evennumseries
    {
        //Print 10 number in series 2,4,6,8,...;
        static void Main(string[] args)
        {
            for (int i = 2; i <= 20; i = i + 2)
            {
              Console.WriteLine(i);
            }
            
        }
    }
}
